// Methods of string

// 1. trim()
// 2. toUpperCase()
// 3. toLowerCase
// 4. slice()

// 1.trim()

let first_name = "           Anita Mishra                     "
console.log(first_name)
console.log(first_name.trim())


// 2. toUpperCase()

let f_name = "anita mishra"
console.log(f_name.toUpperCase())


// 3. toLowerCase

let fname = "ANITA MISHRA"
console.log(fname.toLowerCase())


// 4. slice()

let fullname = "Anita H Mishra"
console.log(fullname.slice(0, 6))